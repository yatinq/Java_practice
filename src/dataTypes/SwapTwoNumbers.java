package dataTypes;

public class SwapTwoNumbers {

	public static void main(String[] args) {
		
		//Before swapping
		int a=5;
		int b=3;
		System.out.println("Before Swap: a = " + a + " b = " + b);
		
		//swapping
		int temp=a;
		a=b;
		b=temp;
		System.out.println("After Swap: a = " + a + " b = " + b);
		

	}

}
